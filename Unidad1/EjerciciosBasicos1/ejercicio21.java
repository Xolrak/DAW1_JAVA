public class ejercicio21 {
    
    public static void main(String[] args)
    { 
        int edad = 20; //a tipo entero 
        System.out.println(edad);
        float km = 7.2f; //b tipo float
        System.out.println(km);
        char letra = 'x'; //c tipo letra(')
        System.out.println(letra);
        double precio = 15.3 / 5 ; //d tipo real
        System.out.println(precio);
        long contador = 3450l; //e int tipo largo (l o L al final)
        System.out.println(contador);
        short horas = 24; //f int tipo corto
        System.out.println(horas);
        byte sino = 127; // g
        System.out.println(sino);
        boolean cena = false; //h tipo boolean
        System.out.println(cena);
        String nombre = "carlos"; //i tipo String (")
        System.out.println(nombre);        
    }

}
